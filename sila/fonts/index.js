// Font styles available
export const fontStyles = {
    normal: normalFont,
    bold: boldFont,
    italic: italicFont,
    monospace: monospaceFont,
    cursive: cursiveFont,
    doubleStruck: doubleStruckFont
};

// Apply font to text
export function applyFont(text, style = 'bold') {
    const font = fontStyles[style];
    if (font) {
        return font(text);
    }
    return text;
}

// Get font style names
export function getFontStyles() {
    return Object.keys(fontStyles);
}

